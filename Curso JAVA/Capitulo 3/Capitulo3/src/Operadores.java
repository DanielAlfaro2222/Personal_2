public class Operadores {
    public static void main(String[] args) {
        /*
            Categoria de operadores en java

            * Operadores aritmeticos
            * Operadores relacionales
            * Operadores logicos
            * Otros operadores(resumidos y de caracteres)
        */

        /*
            Operadores aritmeticos

            + => Suma
            - => Resta
            * => Multiplicacion
            / => Division
            % => Residuo
        */

        /*
            Operadores relacionales

            <  | Menor que
            >  | Mayor que
            <= | Menor o igual que
            >= | Mayor o igual que
            != | Distinto de 
            == | Igual a
        */

        /*
            Operadores logicos

            && => Operador logico and
            || => Operador logico or
            ! => Operador logico de negacion
        */

        /*
            Otros operadores

            ++ => Incremento abreviado
            += => Otra forma de incremento abreviado
            -= => Decremento abreviado
            -- => Otra forma de realizar el decremento
            *= => Multiplicacion abreviada
            /= => Division abreviada
        */
    }
}
