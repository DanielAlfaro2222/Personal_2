public class Constantes {
    public static void main(String[] args) throws Exception {
        /*
            Constantes

            En java las constantes son variables que almacenan valores que no cambian durante la ejecucion de todo el algoritmo, son utiles para almacenar valores fijos que necesitemos en el programa.

            Para definir una variable que va a ser constante en java, tendremos que usar la palabra reservada final, luego el tipo de dato de la variable, luego el nombre y por ultimo el valor que va a almacenar.

            Por ejemplo

            final int Num = 10;
        
        */

        final int Num = 10;
        System.out.println("La variable Num es una constante y almacena el valor: " + Num);
    }
}
